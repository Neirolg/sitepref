Aprenda a calcular os riscos
Às vezes o principal risco é o simples fato de ainda não estarmos preparados para uma licitação pública. Temos de avaliar a nossa capacidade produtiva, os nossos custos, a questão da entrega etc. Além disso, às vezes, desconsideramos o nosso fluxo de produção e utilizamos a mercadoria de um cliente para outro sem pensarmos na reposição do estoque, ou perdermos um prazo ou, ainda, entregarmos um produto fora da especificação. Tudo isso pode nos trazer um enorme problema quando estamos trabalhando com licitações. Cada um desses pequenos problemas pode acarretar sanções e multas. Por isso, cada novo edital de licitação merece uma análise de riscos.

Alto: é considerado alto risco quando a opção "SIM" for marcada até 6 vezes

O que fazer?

1. Não participar da licitação ou do processo de aquisição. 

2. Procurar processos de valores mais baixos ou com condições objetivas em que a sua oferta possa ser a mais competitiva.

 3. Tentar concorrer apenas em itens de uma licitação e não no processo como um todo. 

 Médio: é considerado médio risco quando a opção "SIM" for marcada de 7 a 8 vezes.

O que fazer?

1. Decidir em grupo se o risco de participar vale a pena. 

2. Prever alternativas, garantindo reserva financeira, estoque, estar preparado para atrasos de pagamento, entre outras coisas.

 3. Participar de diferentes licitações e de processos com valores mais baixos (Cotações Eletrônicas) e fazer ofertas apenas em itens de licitação. 

 Baixo: é considerado baixo risco quando a opção "SIM" for marcada de 9 a 10 vezes.

O que fazer?

1. Participar da licitação. 

2. Procurar apreender, ganhando processos de valores mais baixos ou concorrendo em itens de licitações e, progressivamente, partir para licitações de valores maiores ou com gestão de contratos. 

3. Repetir o processo em editais diagnosticados de baixo risco para tornar a licitação uma fonte recorrente de lucro, com baixo risco. 

