Um dos principais desafios do mercado de trabalho sempre foi apresentar os profissionais para as empresas. Muitas vezes, as empresas possuem vagas que não são preenchidas, e do outro lado, muitos profissionais não conseguem estabelecer uma conexão com as empresas e têm dificuldade para conseguir uma colocação.

Para aproximar os profissionais que procuram uma oportunidade no mercado de trabalho e as empresas que buscam novos talentos para suas equipes foi criada a plataforma Emprega Palhoça. "Nós queremos estabelecer este canal de comunicação entre os palhocenses que estão procurando emprego e as empresas que estão precisando de mão de obra em Palhoça. Assim, estaremos contribuindo para a geração de emprego e renda para a população local", define o prefeito Eduardo Freccia.

Atualmente, o banco de dados da plataforma já conta com mais de 20 mil currículos cadastrados e há cerca de 200 vagas abertas em diferentes áreas de atuação, como: recepcionista de hotel, ajudante de pedreiro, jovem aprendiz, analista de departamento pessoal e atendente de call center.

Para as empresas que querem utilizar a plataforma, são muitas as vantagens: a divulgação das vagas é totalmente gratuita; o nome da empresa não é divulgado; não existe um limite de vagas por empresa; a empresa receberá os currículos por e-mail, já com uma triagem prévia.

Por ser uma plataforma que age de forma direta em prol da empregabilidade, não existe nenhum envolvimento financeiro. As empresas que optarem em utilizar os serviços têm como única contrapartida informar quem foi contratado e os motivos para os demais não terem sido aproveitados. "Esta informação é importante porque os candidatos não selecionados vão passar por cursos para aperfeiçoar o currículo, dentro do programa Qualifica Palhoça. É uma forma que nós encontramos para credenciar esses candidatos às próximas vagas que venham a ser ofertadas", explica a secretária de Desenvolvimento Econômico de Palhoça, Anna Paula Heiderscheidt.

Para obter informações mais detalhadas, basta acessar o site da Emprega Palhoça.

