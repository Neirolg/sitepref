Sobre a Sala do Empreendedor de Palhoça
A Sala do Empreendedor é um espaço criado dentro da Prefeitura Municipal de Palhoça para atender exclusivamente microempreendedores individuais e empresas.

A Sala do Empreendedor está instalada na Secretaria da Fazenda do município, no Centro Administrativo Municipal, e atende a população das 7h às 19h, sem necessidade de agendamento prévio - à exceção de projetos específicos, como as consultorias gratuitas.

(BOTAO) Acesse Nossa Agenda

Serviços:
Execução:
Formalização do MEI;
Regularização mobiliária (cadastral/documental/econômica).

Consulta Jurídica
Interpretação do código tributário municipal e suas manutenções;
Denotação do ambiente empresarial nos entes federativos.

Instrução:
1° Nota fiscal eletrônica;
Acesso e conhecimento ao portal do MEI;
Trâmites e ações processuais referentes ao mobiliário;
