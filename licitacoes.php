Como participar do processo de licitação?
1. Faça o curso gratuito de Compras Governamentais para fornecedor do SEBRAE

O Curso do Fornecedor foi elaborado com a indicação clara de quais leis precisam ser lidas e de como devemos analisar os editais de licitação. O curso pode ser feito de forma presencial (busque o SEBRAE) ou a distância, pela internet. 

BOTÃO INSCREVA-SE NO CURSO ONLINE
https://www.sebrae.com.br/sites/PortalSebrae/cursosonline/compras-governamentais-gestores-publicos,d3f37a0655786710VgnVCM1000004c00210aRCRD

2. Aprenda a calcular os riscos
Pequenos problemas podem acarretar sanções e multas. Por tanto, a cada novo edital de licitação merece uma análise de bem aprofundada de riscos. 

BOTÃO AVALIAÇÃO DE riscos
avaliacaoderiscos.php

3. Cadastre-se no Portal de Compras ou no processo de Credenciamento como fornecedor
O Sistema de Cadastro de Fornecedores - SICAF é o Sistema que permite que fornecedores de todo o Brasil e mundo possam se cadastrar e ter acesso a Compras realizadas pelos órgãos públicos. Para participação em licitações públicas é necessário o cadastro no SICAF.

BOTÃO INSCREVA-SE COMO Fornecedor
https://www.comprasnet.gov.br/seguro/loginPortal.asp

4. Leia e estude o edital antes de participar de uma licitação
BOTÃO CONSULTAR EDITAIS EM ABERTO
https://palhoca.atende.net/autoatendimento/servicos/consulta-de-licitacoes/detalhar/1

Lembre-se, os principais pontos para ter êxito em uma licitação, é você estar em dia com suas obrigações empresariais e ler o edital para saber se esta adequo aquela licitação.


