Qualifica Palhoça
O Qualifica Palhoça é um projeto que tem a missão de proporcionar qualificação e valorização profissional aos munícipes e servidores públicos do município, aperfeiçoando as habilidades dos profissionais e reduzindo a distância entre os trabalhadores e as necessidades do mercado.

O objetivo é facilitar o acesso ao mercado de trabalho, oferecendo capacitações através de cursos, palestras e oficinas aos candidatos a vagas de emprego e aos participantes do programa Jovem Aprendiz no município de Palhoça.
