Agenda Sala do Empreendedor 2023

Consultorias gratuitas
A Sala do Empreendedor juntamente com seus parceiros realiza consultorias gratuitas. Atualmente essas consultorias tem ocorrido nas áreas de Atendimento ao MEI, Finanças e Empreendedorismo, RH, Plano de Negócios e Planejamento Estratégico.

O agendamento deve ser feito com antecedência por e-mail ou telefone (com exceção do atendimento com o contador que deve ser feito pelo link) e as consultorias podem ser presenciais na Sala do Empreendedor, ou Online via vídeo chamada. 

Crédito guiado e percepção da saúde financeira - Banco do Empreendedor

O Banco do Empreendedor, em virtude da prospecção de mercado, retomada de investimentos e oferta de crédito a juros zero está realizando capacitações voltadas ao entendimento da saúde financeira das empresas em conformidade com programas governamentais como o Juros Zero.

Consultoria de Finanças e Empreendedorismo - SEBRAE

As consultorias de finanças ofertadas pelo SEBRAE são direcionadas a quem procura organizar a saúde financeira da sua empresa, traçar estratégias para se manter ativo no mercado, além de propor conhecer o próprio negócio.

Empreendedorismo, Recursos Humanos e Planejamento de Negócios - FMP Jr.
A empresa de Consultoria Junior ligada a Faculdade Municipal de Palhoça no projeto de extensão dos acadêmicos de administração buscam em conjunto com o corpo técnico da instituição incursões de consultoria nas áreas de gestão de negócios, pessoas e planejamento de negócio, visando otimizar o operacional das micro e pequenas empresas. 

Atendimento com contador

O SESCON GF disponibiliza gratuitamente atendimento com contador para microempreendedores individuais que precisarem de auxílio na formalização, alteração e baixa do MEI, bem como emissão, atualização ou parcelamento de guia DAS e declaração anual do MEI.

Fica localizado na Av. Das águias 231, Pedra Branca (edifício INAITEC) Térreo, na Secretaria de Desenvolvimento. O atendimento é das das 13:30 as 17:30 nos dias 15/03, 22/03, 29/03, 06/04, 19/04, 17/05, 14/06, 21/06, 05/07, 12/07, 06/08, 16/08.

